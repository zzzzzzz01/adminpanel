    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name_uz' => 'required',
            'name_ru' => 'required',
            'name_en' => 'required',
        ]);

        $data = $request->all();
        $data['slug'] = \Str::slug($request->name_en);

        Tag::create($data);

        return redirect()->route('posts.index')->with('createTag', __('words.tag.create.success'));
    }

